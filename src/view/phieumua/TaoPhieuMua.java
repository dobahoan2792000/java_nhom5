    public void timKiemSV() {
        conn = ConnectToSql.connectDb();
        String idSV = jTextMaSinhVien.getText();
        String sql = "select * from SinhVien where MaSinhVien='" + idSV + "'";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                jTextTenSinhVien.setText(rs.getString(2));
                jTextNganhHoc.setText(rs.getString(3));
                jTextNgaySinh.setText(rs.getString(4));
                jTextGioiTinh.setText(rs.getString(5));
                jTextSDT.setText(rs.getString(6));
                jTextEmail.setText(rs.getString(7));
                rs.close();
                pst.close();
                ConnectToSql.closeConnect();
            } else {
                JOptionPane.showMessageDialog(null, "Không có sinh viên này");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
