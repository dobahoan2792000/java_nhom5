    private void jButtonAddGioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddGioActionPerformed
        if (jTextTenSach.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn phải tìm sách trước");
        } else if (Integer.parseInt(jTextSoLuong.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Sách hiện đã được mua hết");
        } else {
           SachClass sach = sc.timKiemSachMua(jTextMaSach.getText());
            String input = JOptionPane.showInputDialog("Nhập số lượng sách", JOptionPane.OK_CANCEL_OPTION);
            if (IntegerValidate.isInteger(input)) {
                if (Integer.parseInt(input) > 0 && Integer.parseInt(input) <= Integer.parseInt(jTextSoLuong.getText())) {
                    if (gioHang.contains(sach)) {
                        gioHang.get(gioHang.indexOf(sach)).setSoLuongMua(gioHang.get(gioHang.indexOf(sach)).getSoLuongMua() + Integer.parseInt(input));
                    } else {
                        sach.setSoLuongMua(Integer.parseInt(input));
                        gioHang.add(sach);
                        xoaTrangSach();
                    }
                    tongTienCoc = 0;
                    for (SachClass s : gioHang) {
                        tongTienCoc += s.getGia() * s.getSoLuongMua();
                    }
                    jLabelTienCoc.setText("Tổng tiền: " + vn.format(tongTienCoc));
                } else {
                    JOptionPane.showMessageDialog(null, "Số lượng không hợp lệ");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Sai định dạng số lượng mua");
            }
            
        }

        loadList();
    }//GEN-LAST:event_jButtonAddGioActionPerformed

    private void jButtonMuonSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMuonSachActionPerformed
        
        if (gioHang.size() == 0) {
            JOptionPane.showMessageDialog(null, "Chưa thêm sách vào giỏ");
        } else if (jTextTenSinhVien.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Chưa có sinh viên");
        } else {
            Date today = new Date(); // khoi tao ngay muon
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(today);
            PhieuMuaClass pm = new PhieuMuaClass();
            pm.setMaSV(jTextMaSinhVien.getText());
            pm.setNgayDat(date);
            if(jRadioChuaTra.isSelected())
            {
                pm.setDaTra(0);
            }
            else{
                pm.setDaTra(1);
            }
            pm.setGhiChu(jTextAreaGhiChu.getText());
            int kq = pmc.themPhieu(pm, gioHang);
            if(kq == 1)
            {
                gioHang.clear();
                loadList();
            }
        }
    }//GEN-LAST:event_jButtonMuonSachActionPerformed

